// Función para obtener los ejercicios de una unidad específica
export const getEjerciciosByUnidad = (unidadId) => {
  return ejerciciosData[unidadId] || [];
};

// Función para obtener un ejercicio específico
export const getEjercicioById = (ejercicioId) => {
  for (const unidadId in ejerciciosData) {
    const ejercicio = ejerciciosData[unidadId].find(e => e.id === ejercicioId);
    if (ejercicio) return ejercicio;
  }
  return null;
};

// Función para marcar un ejercicio como completado
export const marcarEjercicioCompletado = (ejercicioId) => {
  for (const unidadId in ejerciciosData) {
    const ejercicioIndex = ejerciciosData[unidadId].findIndex(e => e.id === ejercicioId);
    if (ejercicioIndex !== -1) {
      ejerciciosData[unidadId][ejercicioIndex].completado = true;
      // Guardar en localStorage
      localStorage.setItem('ejerciciosData', JSON.stringify(ejerciciosData));
      break;
    }
  }
};

// Función para alternar el estado de completado de un ejercicio
export const toggleEjercicioCompletado = (ejercicioId) => {
  for (const unidadId in ejerciciosData) {
    const ejercicioIndex = ejerciciosData[unidadId].findIndex(e => e.id === ejercicioId);
    if (ejercicioIndex !== -1) {
      ejerciciosData[unidadId][ejercicioIndex].completado = !ejerciciosData[unidadId][ejercicioIndex].completado;
      // Guardar en localStorage
      localStorage.setItem('ejerciciosData', JSON.stringify(ejerciciosData));
      break;
    }
  }
}; 